
class Solution:
    def modifyString(self, s: str) -> str:

        temp = list('qwertyuiopasdfghjklzxcvbnm')
        ans = list('0'+s+'0')

        for i in range(len(ans)):
            if ans[i] == '?':
                j = 0
                while j < len(temp):
                    if temp[j] not in [ans[i-1], ans[i+1]]:
                        ans[i] = temp[j]
                        break
                    else:
                        j += 1

        return ''.join(ans[1:-1])
